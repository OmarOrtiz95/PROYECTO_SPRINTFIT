var NumFor, contador;
NumFor = prompt("Por favor digite un número");
for(contador=1;contador<=NumFor; contador++){ //Para contador =1 hasta contador <= número digitado usuario contador++
    console.log(contador," x ", NumFor, " = ", contador*NumFor);
    document.write("<br/>",contador," x ", NumFor, " = ", contador*NumFor);
}
